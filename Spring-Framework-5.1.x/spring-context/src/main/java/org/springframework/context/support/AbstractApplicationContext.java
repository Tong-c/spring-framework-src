	@Override
	public void refresh() throws BeansException, IllegalStateException {
		//private final Object startupShutdownMonitor = new Object();
		//加同步监视器锁，确保只有一个线程在初始化IOC容器，不然就乱套了
		synchronized (this.startupShutdownMonitor) {
			/**
			 * 1、容器刷新前的一些准备工作:
			 * 	a.设置容器的启动时间
			 * 	b.设置活跃状态active为true
			 *  c.设置关闭状态为false
			 *  d.获取Environment环境对象，并加载当前系统的属性值到Environment环境对象中，在Spring启动的时候提前对必需的变量进行存在性验证
			 *  e.准备监听器和事件的集合对象，默认为空的集合
			 */
			prepareRefresh();

			/**
			 * 2、创建bean工厂：DefaultListableBeanFactory，并进行XML文件读取，这步完成后，配置文件就会解析成一个个 Bean定义，注册到 BeanFactory中
			 *  经过obtainFreshBeanFactory()后，xml文件中的bean定义信息已经被解析封装到返回的是DefaultListableBeanFactory的成员属性中了， 如beanDefinitionMap、beanDefinitionNames
			 */
			ConfigurableListableBeanFactory beanFactory = obtainFreshBeanFactory();

			/**
			 * 3、准备Bean工厂,给Bean工厂的属性赋值,添加几个 BeanPostProcessor，手动注册几个特殊的 bean
			 */
			prepareBeanFactory(beanFactory);

			try {
				/**
				 * 4、空实现，留给子类去扩展额外的处理
				 * 例如，可以添加一些BeanFactoryPostProcessor、BeanPostProcessor、注册额外的bean定义信息等
				 */
				postProcessBeanFactory(beanFactory);

				/**
				 * 5、执行BeanFactoryPostProcessor后置处理器的postProcessBeanFactory()增强方法
				 */
				invokeBeanFactoryPostProcessors(beanFactory);

				/**
				 * 6、注册BeanPostProcessor，注意，这里还不会执行BeanPostProcessor对应的增强方法
				 *  真正调用是在bean初始化前（postProcessBeforeInitialization ）、初始化后（postProcessAfterInitialization）
				 */
				registerBeanPostProcessors(beanFactory);

				/**
				 * 7、为上下文初始化MessageSource，即不同语言的消息体，国际化处理
				 */
				initMessageSource();

				/**
				 * 8、初始化事件多播器
				 */
				initApplicationEventMulticaster();

				/**
				 * 9、模板方法，留给子类初始化其他的bean
				 */
				onRefresh();

				/**
				 * 10、注册监听器
				 */
				registerListeners();

				/**
				 * 11、实例化所有非懒加载的单例Bean
				 */
				finishBeanFactoryInitialization(beanFactory);

				/**
				 * 12、完成上下文的刷新
				 */
				finishRefresh();
			}

			catch (BeansException ex) {
				if (logger.isWarnEnabled()) {
					logger.warn("Exception encountered during context initialization - " +
							"cancelling refresh attempt: " + ex);
				}

				// Destroy already created singletons to avoid dangling resources.
				destroyBeans();

				// Reset 'active' flag.
				cancelRefresh(ex);

				// Propagate exception to caller.
				throw ex;
			}

			finally {
				// Reset common introspection caches in Spring's core, since we
				// might not ever need metadata for singleton beans anymore...
				resetCommonCaches();
			}
		}
	}

	/**
	 * 为刷新准备上下文，设置其启动日期和active标志以及执行属性源的任何初始化
	 */
	protected void prepareRefresh() {
		// 1、设置启动时间、一些标志位

		// 设置容器的启动时间
		this.startupDate = System.currentTimeMillis();
		// 设置容器的关闭标志位
		this.closed.set(false);
		// 设置容器的激活标志位
		this.active.set(true);

		if (logger.isDebugEnabled()) {
			if (logger.isTraceEnabled()) {
				logger.trace("Refreshing " + this);
			}
			else {
				logger.debug("Refreshing " + getDisplayName());
			}
		}

		// 2、初始化占位符属性源，留给子类实现
		// 例如，可以通过重写此方法, 设置应用启动时必须的一些属性
		initPropertySources();

		// Validate that all properties marked as required are resolvable:
		// see ConfigurablePropertyResolver#setRequiredProperties

		// 3、验证并获取环境对象，验证容器启动必须依赖的属性是否都已经放入环境对象中
		// getEnvironment()创建属性源StandardEnvironment时通过System.getProperties()、System.getenv()对属性、环境变量进行了初始化
		getEnvironment().validateRequiredProperties();

		// 4、判断早期事件监听器集合是否为空，如果为空，则将监听器添加到此集合中
		if (this.earlyApplicationListeners == null) {
			this.earlyApplicationListeners = new LinkedHashSet<>(this.applicationListeners);
		}
		else {
			// Reset local application listeners to pre-refresh state.

			//如果不为空，清空集合
			this.applicationListeners.clear();
			this.applicationListeners.addAll(this.earlyApplicationListeners);
		}

		// Allow for the collection of early ApplicationEvents,
		// to be published once the multicaster is available...

		// 创建一个容器用于保存早期待发布的事件集合
		this.earlyApplicationEvents = new LinkedHashSet<>();
	}

